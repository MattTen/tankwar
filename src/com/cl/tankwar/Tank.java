package com.cl.tankwar;

import java.awt.*;

/**
 * @Author: Matt Ten
 * @Date: 2020/8/9 23:09
 * @Description: com.cl.tankwar
 * @Version: 1.0
 */
public class Tank {
    private int x, y;
    private Dir dir = Dir.DOWN;
    public static final int SPEED = 10;
    private boolean moving = false;

    public Tank(int x, int y, Dir dir) {
        this.x = x;
        this.y = y;
        this.dir = dir;
    }

    public Dir getDir() {
        return dir;
    }

    public void setDir(Dir dir) {
        this.dir = dir;
    }

    public void paint(Graphics g) {
        g.fillRect(x, y,50, 50);
        switch (dir) {
            case LEFT:
                x -= SPEED;
                break;
            case UP:
                y -= SPEED;
                break;
            case RIGHT:
                x += SPEED;
                break;
            case DOWN:
                y += SPEED;
                break;
            default:
                break;
        }
    }
}
